# (pid, relevance 0-3, reason). Listed best-to-worst inside each query.
GT = list[tuple[str, int, str]]


def Q(qid: str, difficulty: str, query_text: str, key_attrs: list[str],
      gt: GT, notes: str | None = None) -> dict:
    rec = {
        "id": qid,
        "difficulty": difficulty,
        "domain": "ski",
        "query_text": query_text,
        "key_attributes": key_attrs,
    }
    if notes:
        rec["notes"] = notes
    # Match v1 convention: only positive-relevance entries appear in
    # ground_truth_top5. Any rel=0 tuples passed in act as authoring
    # comments / contrastive negatives and are filtered out here.
    rec["ground_truth_top5"] = [
        {"product_id": p, "relevance": r, "reason": s}
        for p, r, s in gt if r > 0
    ]
    return rec


