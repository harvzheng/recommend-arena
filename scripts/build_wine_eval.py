def _cache_open() -> sqlite3.Connection:
    CACHE_PATH.parent.mkdir(parents=True, exist_ok=True)
    conn = sqlite3.connect(str(CACHE_PATH))
    conn.execute("CREATE TABLE IF NOT EXISTS cache (key TEXT PRIMARY KEY, value TEXT)")
    return conn


def _cache_get(conn, key):
    row = conn.execute("SELECT value FROM cache WHERE key=?", (key,)).fetchone()
    return row[0] if row else None


def _cache_put(conn, key, value):
    conn.execute("INSERT OR REPLACE INTO cache (key, value) VALUES (?, ?)", (key, value))
    conn.commit()


def _load_products() -> list[dict]:
    return [json.loads(line) for line in PRODUCTS_PATH.read_text().splitlines() if line.strip()]


def _format_catalog_for_prompt(products: list[dict]) -> str:
    lines = []
    for p in products:
        a = p["attributes"]
        s = p["specs"]
        vintage = s.get("vintage") or "NV"
        lines.append(
            f'{p["id"]}: {p["name"]} | winery={p["brand"]} | variety={a.get("variety","")} '
            f'| country={a.get("country","")} | region={a.get("region","")} '
            f'| points={a.get("points","")} | price=${a.get("price","")} | vintage={vintage}'
        )
    return "\n".join(lines)


def _openrouter_call(model: str, prompt: str, api_key: str) -> str:
    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json",
    }
    payload = {
        "model": model,
        "messages": [
            {"role": "system",
             "content": "Respond ONLY with valid JSON. No markdown fences, no commentary."},
            {"role": "user", "content": prompt},
        ],
        "max_tokens": 2048,
        "temperature": 0.0,
    }
    last_err = None
    for attempt in range(3):
        try:
            r = httpx.post(OPENROUTER_URL, headers=headers, json=payload, timeout=120.0)
            r.raise_for_status()
            data = r.json()
            return data["choices"][0]["message"]["content"]
        except (httpx.HTTPError, KeyError) as e:
            last_err = e
            wait = 2 ** attempt
            logger.warning("openrouter call failed (%s); retry in %ds", e, wait)
            time.sleep(wait)
    raise RuntimeError(f"openrouter call failed after retries: {last_err}")


def _strip_fences(text: str) -> str:
    text = text.strip()
    if text.startswith("```"):
        text = text.strip("`")
        if text.lower().startswith("json"):
            text = text[4:]
        text = text.strip()
    return text


def _grade_query(model: str, api_key: str, query: dict, catalog_str: str,
                 valid_ids: set[str], conn) -> list[dict]:
    prompt = (
        "You are a wine expert grading a recommendation system's ground truth.\n\n"
        f"User query: {query['query_text']!r}\n"
        f"Difficulty: {query['difficulty']}\n"
        f"Key attributes the user cares about: {query['key_attributes']}\n\n"
        "Catalog (one wine per line, with id):\n"
        f"{catalog_str}\n\n"
        "Pick exactly 5 wines from the catalog that best match the query. "
        "For each, give:\n"
        "  - product_id (the WINE-### id from the catalog)\n"
        "  - relevance: 3 (perfect match), 2 (strong match), 1 (partial match)\n"
        "  - reason: <=120 chars, grounded in the wine's attributes\n\n"
        "Return strict JSON: a list of 5 objects with keys product_id, relevance, reason. "
        "Sort best->worst. No prose, no markdown - JSON only."
    )
    key = hashlib.sha256(f"{model}::{prompt}".encode()).hexdigest()
    cached = _cache_get(conn, key)
    if cached:
        logger.info("[cache] %s", query["id"])
        return json.loads(cached)
    logger.info("[teacher %s] %s", model, query["id"])
    raw = _openrouter_call(model, prompt, api_key)
    text = _strip_fences(raw)
    try:
        parsed = json.loads(text)
    except json.JSONDecodeError as e:
        logger.error("non-JSON from teacher for %s; raw=%r", query["id"], raw[:300])
        raise
    if isinstance(parsed, dict):
        # Some models wrap the array in {"results": [...]} — pick the largest list value.
        list_vals = [v for v in parsed.values() if isinstance(v, list)]
        if list_vals:
            parsed = max(list_vals, key=len)
    if not isinstance(parsed, list) or len(parsed) != 5:
        raise ValueError(f"expected list of 5, got: {parsed!r}")
    for entry in parsed:
        pid = entry["product_id"]
        if pid not in valid_ids:
            # Tolerate de-padded ids: WINE-10 -> WINE-010.
            m = pid.split("-", 1)
            if len(m) == 2 and m[1].isdigit():
                padded = f"{m[0]}-{int(m[1]):03d}"
                if padded in valid_ids:
                    entry["product_id"] = padded
                    pid = padded
        if pid not in valid_ids:
            raise ValueError(f"unknown id from teacher: {entry!r}")
        if entry["relevance"] not in (1, 2, 3):
            raise ValueError(f"bad relevance: {entry!r}")
    _cache_put(conn, key, json.dumps(parsed))
    return parsed


def main() -> int:
    logging.basicConfig(level=logging.INFO, format="%(message)s")
    p = argparse.ArgumentParser()
    p.add_argument("--model", default=DEFAULT_MODEL)
    args = p.parse_args()

    if not PRODUCTS_PATH.exists():
        logger.error("missing %s - run scripts/build_wine_dataset.py first", PRODUCTS_PATH)
        return 2
    api_key = os.environ.get("OPENROUTER_API_KEY")
    if not api_key:
        logger.error("set OPENROUTER_API_KEY in env")
        return 2

    products = _load_products()
    valid_ids = {p["id"] for p in products}
    catalog_str = _format_catalog_for_prompt(products)

    conn = _cache_open()
    out_queries = []
    for q in QUERIES:
        gt = _grade_query(args.model, api_key, q, catalog_str, valid_ids, conn)
        out_q = dict(q)
        out_q["ground_truth_top5"] = gt
        out_queries.append(out_q)

    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUT_PATH.write_text(json.dumps({"version": "1.0", "queries": out_queries}, indent=2))
    logger.info("wrote %s (%d queries, model=%s)", OUT_PATH, len(out_queries), args.model)
    return 0


if __name__ == "__main__":
    sys.exit(main())
